declare(strict_types=1);

namespace BaksDev\Yandex\Market\Products\UseCase\Cards\NewEdit\Parameters;

use BaksDev\Yandex\Market\Products\Entity\Card\Parameters\YaMarketProductsCardParametersInterface;
use Symfony\Component\Validator\Constraints as Assert;

/** @see YaMarketProductsCardParameters */
final class YaMarketProductsCardParametersDTO implements YaMarketProductsCardParametersInterface
{

    /**
     * Наименование характеристики
     */
    #[Assert\NotBlank]
    private string $type;

    /**
     * Пользовательское значение
     */
    private ?string $value = null;

    /**
     * Type
     */
    public function getType(): string
    {
        return $this->type;
    }

    public function setType(string $type): self
    {
        $this->type = $type;
        return $this;
    }



    /**
     * Value
     */
    public function getValue(): string
    {
        return $this->value;
    }

    public function setValue(string $value): self
    {
        $this->value = $value;
        return $this;
    }
}